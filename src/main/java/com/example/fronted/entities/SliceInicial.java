package com.example.fronted.entities;

public class SliceInicial {
    private String nombre;
    private String topologia;
    private String numeroVM;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTopologia() {
        return topologia;
    }

    public void setTopologia(String topologia) {
        this.topologia = topologia;
    }

    public String getNumeroVM() {
        return numeroVM;
    }

    public void setNumeroVM(String numeroVM) {
        this.numeroVM = numeroVM;
    }
}
